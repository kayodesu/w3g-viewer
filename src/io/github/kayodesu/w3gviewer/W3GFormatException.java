public class W3GFormatException extends Exception {


    public W3GFormatException() {
        super();
    }

    public W3GFormatException(String message) {
        super(message);
    }

    public W3GFormatException(String message, Throwable cause) {
        super(message, cause);
    }

    public W3GFormatException(Throwable cause) {
        super(cause);
    }
}
